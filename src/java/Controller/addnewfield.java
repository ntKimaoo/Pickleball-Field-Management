    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
         BaseDAO bdao=new BaseDAO();
         request.setAttribute("listbase", bdao.getAll());
         request.getRequestDispatcher("addnewfield.jsp").forward(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        FieldDAO fdao=new FieldDAO();
        String base=request.getParameter("base");
        String cost=request.getParameter("cost");
        String vip=request.getParameter("vip");
        String image=request.getParameter("image");
        boolean realvip=true;
        System.out.println(base+cost+vip+image);
        if (vip==null||vip.equals("")) realvip=false;
        Field f=new Field(0, Integer.parseInt(cost), "online", realvip, base, image);
        fdao.insertField(f);
        response.sendRedirect("fieldmanagement");
    }

